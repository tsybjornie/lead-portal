/**
 * Payment Milestones  Gated by Quality Checkpoints
 * 
 * Payments are tracked (not processed) through the app.
 * Each milestone unlocks ONLY when the corresponding quality gate passes.
 * 10% retention held until final handover inspection.
 * 
 * Lives in: Roof (financial tracking) + Roof (milestone management)
 */

import { TradeCategory } from './trades';

// ============================================================
// PAYMENT MILESTONE
// ============================================================

export type MilestoneStatus =
    | 'upcoming'          // Not yet due
    | 'due'               // Quality gate passed, payment expected
    | 'overdue'           // Quality gate passed but not paid
    | 'paid'              // Payment received
    | 'blocked'           // Quality gate hasn't passed  can't pay yet
    | 'disputed';         // Client disputes quality

export type MilestoneTrigger =
    | 'contract_signed'       // Deposit
    | 'demolition_complete'   // After demo
    | 'masonry_complete'      // After wet works
    | 'carpentry_installed'   // After carpentry onsite (requires factory QC + delivery inspection)
    | 'painting_complete'     // After painting
    | 'handover_inspection'   // Final walkthrough  workmanship checklist all 
    | 'defect_free_period'    // After 30-day defect liability period
    | 'custom';               // Designer defines custom milestone

export interface PaymentMilestone {
    id: string;
    projectId: string;
    milestoneNumber: number;         // Sequential (1, 2, 3...)

    // What triggers this payment
    name: string;                    // e.g. "Progressive Payment 2  Wet Works"
    description?: string;
    trigger: MilestoneTrigger;
    customTriggerDescription?: string;

    // Amount
    percentage: number;              // % of total contract value
    amount: number;                  // Absolute amount (SGD)
    gst?: number;                    // 9% GST if registered

    // Quality gate
    qualityGateId?: string;          // Links to checkpoint/checklist
    qualityGatePassed: boolean;      // Must be true before payment unlocks
    qualityGateType?: 'workmanship_checklist' | 'factory_qc' | 'delivery_inspection' | 'post_install' | 'handover';

    // Trades involved in this milestone
    tradesIncluded: TradeCategory[];

    // Status
    status: MilestoneStatus;
    dueDate?: string;

    // Payment tracking (not processing  just recording)
    paymentMethod?: 'paynow' | 'bank_transfer' | 'cheque' | 'cash' | 'credit_card';
    paymentReference?: string;       // Transaction reference
    paymentProofUrl?: string;        // Screenshot/receipt upload
    paidAt?: string;
    paidAmount?: number;             // May differ slightly from amount
    confirmedBy?: string;            // Designer confirms receipt

    // Invoice
    invoiceNumber?: string;
    invoiceUrl?: string;             // PDF generated by Roof
    invoiceDate?: string;
}

// ============================================================
// PAYMENT SCHEDULE  Full milestone schedule for a project
// ============================================================

export interface PaymentSchedule {
    projectId: string;
    clientName: string;
    contractValue: number;           // Total contract value
    currency: 'SGD' | 'MYR';

    milestones: PaymentMilestone[];

    // Computed
    totalPaid: number;
    totalRemaining: number;
    paidPercentage: number;
    retentionAmount: number;         // Held until final handover
    retentionPercentage: number;     // Typically 10%

    // Invoice tracking
    totalInvoiced: number;
    totalOutstanding: number;

    lastUpdated: string;
}

