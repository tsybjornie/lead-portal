/** Generate payment schedule from template */
export function generatePaymentSchedule(
    projectId: string,
    clientName: string,
    contractValue: number,
    currency: PaymentSchedule['currency'],
    template: MilestoneTemplate[],
): PaymentSchedule {
    const milestones: PaymentMilestone[] = template.map((t, i) => ({
        id: `ms_${projectId}_${i + 1}`,
        projectId,
        milestoneNumber: i + 1,
        name: t.name,
        description: t.description,
        trigger: t.trigger,
        percentage: t.percentage,
        amount: Math.round(contractValue * (t.percentage / 100) * 100) / 100,
        qualityGatePassed: false,
        qualityGateType: t.qualityGateType,
        tradesIncluded: t.tradesIncluded,
        status: i === 0 ? 'due' : 'upcoming',
    }));

    const retentionMilestone = milestones.find(m => m.trigger === 'handover_inspection');

    return {
        projectId,
        clientName,
        contractValue,
        currency,
        milestones,
        totalPaid: 0,
        totalRemaining: contractValue,
        paidPercentage: 0,
        retentionAmount: retentionMilestone?.amount || contractValue * 0.10,
        retentionPercentage: retentionMilestone?.percentage || 10,
        totalInvoiced: 0,
        totalOutstanding: 0,
        lastUpdated: new Date().toISOString(),
    };
}

/** Check if a milestone can be unlocked */
export function canUnlockMilestone(milestone: PaymentMilestone): { canUnlock: boolean; reason?: string } {
    if (milestone.status === 'paid') {
        return { canUnlock: false, reason: 'Already paid' };
    }
    if (!milestone.qualityGatePassed && milestone.qualityGateType) {
        return {
            canUnlock: false,
            reason: `Quality gate not passed: ${milestone.qualityGateType.replace(/_/g, ' ')}. Complete the ${milestone.qualityGateType === 'handover' ? 'final walkthrough checklist' : 'workmanship checklist'} first.`,
        };
    }
    return { canUnlock: true };
}

/** Calculate client-facing payment summary */
export function getPaymentSummary(schedule: PaymentSchedule): {
    paid: number;
    remaining: number;
    percentage: number;
    nextMilestone?: { name: string; amount: number; status: string };
    retention: number;
} {
    const paid = schedule.milestones
        .filter(m => m.status === 'paid')
        .reduce((s, m) => s + (m.paidAmount || m.amount), 0);

    const next = schedule.milestones.find(m => m.status === 'due' || m.status === 'upcoming');

    return {
        paid,
        remaining: schedule.contractValue - paid,
        percentage: Math.round((paid / schedule.contractValue) * 100),
        nextMilestone: next ? { name: next.name, amount: next.amount, status: next.status } : undefined,
        retention: schedule.retentionAmount,
    };
}
